    private void btn_LoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_LoginActionPerformed
        String pws = new String(password.getPassword());
        LoggerData.initLogger();
        Logger logger = Logger.getLogger("loggerdata");

        if (userName.getText().equals("$Admin") && pws.equals("$_1234")) {   // Static user access

            Main mainMenu = new Main("Admin", "$Admin", "Static_User");
            mainMenu.setVisible(true);
            this.dispose();

        } else {
            try {
                String un = userName.getText();
                String pw = HashingEncription.getEncription(pws);
                String userdata = ("SELECT * FROM user WHERE user_name=? AND password=? AND state=1");
                PreparedStatement ps = DB.getConnection().prepareStatement(userdata);
                ps.setString(1, un);
                ps.setString(2, pw);
                ResultSet rs = ps.executeQuery();

                if (rs.next()) {
                    warningMsg.setText(null);
                    logger.info(un + " Login to System");
                    Main m = new Main(rs.getString("user_type"), rs.getString("id_employee"), rs.getString("user_name"));
                    m.setVisible(true);
                    this.dispose();
                } else {
                    warningMsg.setText("Invalid user name or password, Please try again");
                    logger.warn(un + " trying to Login with password " + pws);
                }

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e);
            }
        }
    }//GEN-LAST:event_btn_LoginActionPerformed

