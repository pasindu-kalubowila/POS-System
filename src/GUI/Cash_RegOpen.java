    private String generateID() {
        String id = null;
        try {
            ResultSet rs = DB.search("SELECT COUNT(id) AS id FROM cash_register");
            if (rs.next()) {
                int rowcount = rs.getInt("id");
                rowcount++;
                id = "CR" + YearMonth.now().getYear() + String.format("%0" + 4 + "d", rowcount);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        return id;
    }
