    private void btn_OpenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_OpenActionPerformed

        if (user_Type.equals("Admin") | user_Type.equals("Manager") | user_Type.equals("Cashier")) {

            if (txt_float.getText() != null) {
                try {
                    DB.iud("INSERT INTO cash_register (id, open_date, open_time, openning_balance, user, status)"
                            + "VALUES ('" + generateID() + "', '" + LocalDate.now() + "', '" + LocalTime.now() + "', '" + txt_float.getText() + "', '" + menu.txt_empName.getText() + "', 'Open')");
                    if (noActivecashReg) {
                        Invoice inv = new Invoice(menu);
                        menu.view.removeAll();
                        menu.view.revalidate();
                        menu.view.repaint();
                        menu.view.add((JPanel) inv.panel);

                    } else {
//                        Cash_Register cr = new Cash_Register(menu, user_Type);
//                        menu.view.removeAll();
//                        menu.view.revalidate();
//                        menu.view.repaint();
//                        menu.view.add((JPanel) cr.panel);

                        ResultSet rs = DB.search("SELECT status,id FROM cash_register");
                        while (rs.next()) {

                            if (rs.getString(1).equals("Open")) {

                                Cash_Register cr = new Cash_Register(menu, user_Type);
                                menu.view.removeAll();
                                menu.view.revalidate();
                                menu.view.repaint();
                                menu.view.add((JPanel) cr.panel);
                                cr.loadCashRegister(rs.getString(2));
                                this.dispose();
                                System.gc();

                            } 
                        }
                    }
                    this.dispose();
                } catch (Exception e) {
                    e.printStackTrace();
                }

            }

        } else if (user_Type.equals("DataEntry")) {
            Access_Denied ad = new Access_Denied();
            JOptionPane.showMessageDialog(this, ad.panel, "Acess Denied", JOptionPane.PLAIN_MESSAGE);
        }
    }//GEN-LAST:event_btn_OpenActionPerformed

