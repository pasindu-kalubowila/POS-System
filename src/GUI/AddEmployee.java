    private void generateID() {
        try {
            ResultSet rs = DB.search("SELECT COUNT(id_employee) AS id FROM employee");
            if (rs.next()) {
                int rowcount = rs.getInt("id");
                rowcount++;
                id_emplyee.setText(String.format("%0" + 4 + "d", rowcount));
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void clear() {
        txt_fname.setText(null);
        txt_lname.setText(null);
        txt_nic.setText(null);
        txt_mName.setText(null);
        txt_email.setText(null);
        txt_mobile.setText(null);
        txt_street.setText(null);
        txt_city.setText(null);
        dob.setDate(null);
    }

    public void updateData(String employeeID) {
        try {
            ResultSet rs = DB.search("SELECT * FROM employee WHERE id_employee = '" + employeeID + "'");
            if (rs.next()) {
                id_emplyee.setText(rs.getString(1));
                txt_fname.setText(rs.getString(2));
                txt_mName.setText(rs.getString(3));
                txt_lname.setText(rs.getString(4));
                if (rs.getString(5).equals("Male")) {
                    male.setSelected(true);
                } else {
                    female.setSelected(true);
                }
                dob.setDate(rs.getDate(6));
                txt_nic.setText(rs.getString(7));
                txt_email.setText(rs.getString(8));
                txt_mobile.setText(rs.getString(9));
                txt_street.setText(rs.getString(10));
                txt_city.setText(rs.getString(11));
                if (rs.getInt(12) == 1) {
                    active.setSelected(true);
                } else {
                    inactive.setSelected(true);
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
