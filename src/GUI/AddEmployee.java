    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        if (!txt_fname.getText().equals("")) {
            if (dob.getDate() != null) {
                String gen;
                if (male.isSelected()) {
                    gen = "Male";
                } else {
                    gen = "Female";
                }
                int status;
                if (active.isSelected()) {
                    status = 1;
                } else {
                    status = 0;
                }

                String dobDate = new SimpleDateFormat("yyyy-MM-dd").format(dob.getDate());

                try {
                    DB.iud("INSERT INTO employee VALUES('" + id_emplyee.getText() + "','" + txt_fname.getText() + "','" + txt_mName.getText() + "','" + txt_lname.getText()
                            + "','" + gen + "','" + dobDate + "','" + txt_nic.getText() + "','" + txt_email.getText() + "','" + txt_mobile.getText() + "','" + txt_street.getText() + "','" + txt_city.getText() + "','" + status + "')");
                    new Save().setVisible(true);
                    clear();
                    generateID();
                     emp.loadTableData();
                } catch (com.mysql.jdbc.exceptions.jdbc4.MySQLIntegrityConstraintViolationException e) {

                    if (JOptionPane.showConfirmDialog(this, "Do you want to update this employee data", "Update Employee Data", JOptionPane.YES_OPTION, JOptionPane.INFORMATION_MESSAGE) == 0) {
                        try {
                            DB.iud("UPDATE employee SET fname='" + txt_fname.getText() + "', mName='" + txt_mName.getText() + "', lname='" + txt_lname.getText()
                                    + "', gender='" + gen + "', dob='" + dobDate + "', nic='" + txt_nic.getText() + "', email='" + txt_email.getText() + "', mobile='" + txt_mobile.getText()
                                    + "', street='" + txt_street.getText() + "', city='" + txt_city.getText() + "',state='" + status + "' WHERE id_employee = '" + id_emplyee.getText() + "'");
                           
                                new Save().setVisible(true);
                                clear();
                                generateID();
                                emp.loadTableData();
                                menu.setEnabled(true);
                            
                            this.dispose();
                        } catch (Exception es) {
                            es.printStackTrace();
                        }
                    }
                } catch (Exception ex) {
                    ex.printStackTrace();
                }
            } else {
                JOptionPane.showMessageDialog(this, "Plaese Enter the Employees' Date Of Birth", "Warning", JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Plaese enter Employee details here before the save", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btn_saveActionPerformed

