    private void txt_RateKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_RateKeyTyped
        if (percen.isSelected() || amount.isSelected()) {
            if (Character.isLetter(evt.getKeyChar())) {
                evt.consume();
            }
            if (percen.isSelected()) {
                Validation.setLength(evt, txt_Rate.getText(), 6);
            } else {
                Validation.setLength(evt, txt_Rate.getText(), 10);
            }
        } else {
            evt.consume();
        }

    }//GEN-LAST:event_txt_RateKeyTyped

    private void txt_RateKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_RateKeyReleased
        if (percen.isSelected()) {
            if (!txt_Rate.getText().isEmpty()) {
                if (Double.parseDouble(txt_Rate.getText()) < 101) {
                    if (digiPrint) {
                        txt_Discount.setText(String.valueOf((digi_amount * Double.parseDouble(txt_Rate.getText())) / 100));
                    } else if (fulldiscount != null) {
                        txt_Discount.setText(String.valueOf((total * Double.parseDouble(txt_Rate.getText())) / 100));
                    } else {
                        txt_Discount.setText(String.valueOf((qty * item_price * Double.parseDouble(txt_Rate.getText())) / 100));
                    }
                }
            }
        } else {
            if (digiPrint) {
                txt_Discount.setText(txt_Rate.getText());
            } else if (fulldiscount != null) {
                txt_Discount.setText(txt_Rate.getText());
            } else {
                txt_Discount.setText(txt_Rate.getText());
            }
        }
    }//GEN-LAST:event_txt_RateKeyReleased

