    private void btn_DoneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_DoneActionPerformed
        if (!txt_Discount.getText().isEmpty()) {
            if (digiPrint) {
                DecimalFormat decim = new DecimalFormat("#,##0.00");
                inv_DigiPrint.discounts(txt_Discount.getText(), decim.format(digi_amount - Double.parseDouble(txt_Discount.getText())), rowNo);
            } else if (fulldiscount != null) {
                double dis = Double.parseDouble(txt_Discount.getText());
                invoice.txt_fulldiscount.setText(deci.format(dis));
                invoice.getNetTotal();
            } else {
                for (int i = 0; i < invoice.table.getRowCount(); i++) {
                    if (Integer.parseInt(invoice.table.getValueAt(i, 0).toString())==rowNo && invoice.table.getValueAt(i, 4).toString().equals(unit)) {
                        double dis = Double.parseDouble(txt_Discount.getText());
                        invoice.table.setValueAt(deci.format(dis* qty), i, 7);
                        invoice.table.setValueAt(deci.format((item_price) - (dis* qty)), i, 8);
//                        invoice.getSubTotal();
                        invoice.getDiscount();
                        invoice.getNetTotal();

                    }
                }
            }
            menu.setEnabled(true);
            this.dispose();
        }
    }//GEN-LAST:event_btn_DoneActionPerformed

