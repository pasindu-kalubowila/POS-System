    private void generateCustomerID() {
        try {
            ResultSet rs = DB.search("SELECT COUNT(id_customer) AS id FROM customer");
            if (rs.next()) {
                int rowcount = rs.getInt("id");
                rowcount++;
                id_customer.setText(String.format("%0" + 6 + "d", rowcount));
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void clear() {
        txt_fname.setText(null);
        txt_lname.setText(null);
        txt_nic.setText(null);
        txt_mobile.setText(null);
        credit_amount.setText(null);
    }

    public void updateData(String customerID) {
        try {
            ResultSet rs = DB.search("SELECT * FROM customer WHERE id_customer = '" + customerID + "'");
            if (rs.next()) {
                id_customer.setText(rs.getString(1));
                txt_fname.setText(rs.getString(2));
                txt_lname.setText(rs.getString(3));
                txt_nic.setText(rs.getString(5));
                txt_mobile.setText(rs.getString(8));
                credit_amount.setText(rs.getString(9));
                credit_limit.setText(rs.getString(10));
                if (rs.getInt(11) == 1) {
                    active.setSelected(true);
                } else {
                    inactive.setSelected(true);
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public void loadHistory(String customerID) {
        try {
            ResultSet rs = DB.search("SELECT * FROM customer_payment c WHERE customer_id = '" + customerID + "'");
            DefaultTableModel dtm = (DefaultTableModel) payhistory.getModel();
            dtm.setRowCount(0);
            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString(2));
                v.add(rs.getString(3));
                v.add(rs.getString(4));
                v.add(rs.getString(5));
                v.add(rs.getString(6));
                dtm.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
