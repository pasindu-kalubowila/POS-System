    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        if (txt_fname.getText().isEmpty()) {
            JOptionPane.showMessageDialog(menu, "You must enter the customer name", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            if (!txt_fname.getText().equals("")) {

                int status;
                if (active.isSelected()) {
                    status = 1;
                } else {
                    status = 0;
                }

                try {
                    DB.iud("INSERT INTO customer (id_customer, fname, lname, nic, mobile, credit_amount, credit_limit, state) VALUES('" + id_customer.getText() + "','" + txt_fname.getText() + "','" + txt_lname.getText() + "',"
                            + "'" + txt_nic.getText() + "','" + txt_mobile.getText() + "','0.0','" + credit_limit.getText() + "','" + status + "')");
                    new Save().setVisible(true);
                    clear();
                    generateCustomerID();
                    cus.loadTableData();
                } catch (com.mysql.jdbc.exceptions.jdbc4.MySQLIntegrityConstraintViolationException e) {
                    if (JOptionPane.showConfirmDialog(this, "Do you want to update this customers data", "Update Customer Data", JOptionPane.YES_OPTION, JOptionPane.INFORMATION_MESSAGE) == 0) {
                        try {
                            DB.iud("UPDATE customer SET fname='" + txt_fname.getText() + "', lname='" + txt_lname.getText() + "', "
                                    + " nic='" + txt_nic.getText() + "', credit_limit='" + credit_limit.getText() + "', mobile='" + txt_mobile.getText() + "', state='" + status + "' WHERE id_customer='" + id_customer.getText() + "'");
                            new Save().setVisible(true);
                            clear();
                            generateCustomerID();
                            menu.setEnabled(true);
                            cus.loadTableData();
                            this.dispose();
                        } catch (Exception x) {
                            x.printStackTrace();
                        }
                    }

                } catch (Exception ex) {
                    ex.printStackTrace();
                }
            } else {
                JOptionPane.showMessageDialog(this, "Plaese Enter data", "Warning", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_btn_saveActionPerformed

