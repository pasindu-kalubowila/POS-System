    private String generateCustomerID() {
        String id = null;
        try {
            ResultSet rs = DB.search("SELECT COUNT(id_customer) AS id FROM customer");
            if (rs.next()) {
                int rowcount = rs.getInt("id");
                rowcount++;
                id = String.format("%0" + 6 + "d", rowcount);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        return id;
    }
