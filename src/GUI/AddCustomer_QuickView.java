    private void btn_DoneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_DoneActionPerformed
        if (!name.getText().isEmpty()) {
            try {
                DB.iud("INSERT INTO customer (id_customer, fname, mobile, credit_amount, credit_limit) VALUES('" + generateCustomerID() + "', '" + name.getText() + "', '" + mobile.getText() + "','0','3000')");
                this.menu.setEnabled(true);

            } catch (Exception e) {
                e.printStackTrace();
            }
            if (inv != null) {
                Invoice invc = new Invoice(menu);
                menu.view.removeAll();
                menu.view.revalidate();
                menu.view.repaint();
                menu.view.add((JPanel) invc.panel);
            } else {
                Invoice_Digital_Printing invc = new Invoice_Digital_Printing(menu);
                menu.view.removeAll();
                menu.view.revalidate();
                menu.view.repaint();
                menu.view.add((JPanel) invc.panel);
            }
            this.dispose();
        }
    }//GEN-LAST:event_btn_DoneActionPerformed

