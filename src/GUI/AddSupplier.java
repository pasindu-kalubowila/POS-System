    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        if (!txt_fname.getText().equals("") | !txt_company.getText().equals("")) {

            int status;
            if (active.isSelected()) {
                status = 1;
            } else {
                status = 0;
            }

            try {
                DB.iud("INSERT INTO supplier VALUES('" + id_supplier.getText() + "','" + txt_fname.getText() + "','" + txt_lname.getText() + "','" + txt_nic.getText() + "','" + txt_company.getText()
                        + "','" + txt_email.getText() + "','" + txt_mobile.getText() + "','" + txt_fax.getText() + "','" + txt_street.getText() + "','" + txt_city.getText() + "','" + status + "' )");
                new Save().setVisible(true);
                clear();
                generateID();
                supplierView.loadTableData();
            } catch (com.mysql.jdbc.exceptions.jdbc4.MySQLIntegrityConstraintViolationException s) {
                if (JOptionPane.showConfirmDialog(this, "Do you want to update this suppliers' data", "Update Supplier Data", JOptionPane.YES_OPTION, JOptionPane.INFORMATION_MESSAGE) == 0) {
                    
                    try {
                        DB.iud("UPDATE supplier SET fname='" + txt_fname.getText() + "', lname='" + txt_lname.getText() + "', nic='" + txt_nic.getText()
                                + "', company='" + txt_company.getText() + "', email='" + txt_email.getText() + "', mobile='" + txt_mobile.getText() + "', fax='" + txt_fax.getText()
                                + "', street='" + txt_street.getText() + "', city='" + txt_city.getText() + "', state='" + status + "' WHERE id_supplier='" + id_supplier.getText() + "' ");
                        new Save().setVisible(true);
                        clear();
                        generateID();

                        menu.setEnabled(true);
                        supplierView.loadTableData();
                        this.dispose();
                    } catch (Exception ex) {
                        ex.printStackTrace();
                    }
                }

            } catch (Exception e) {
                e.printStackTrace();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Plaese enter Suppliers details here before the save", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btn_saveActionPerformed

