    private void generateID() {
        try {
            ResultSet rs = DB.search("SELECT COUNT(id_supplier) AS id FROM supplier");
            if (rs.next()) {
                int rowcount = rs.getInt("id");
                rowcount++;
                id_supplier.setText("IS" + String.format("%0" + 6 + "d", rowcount));
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void clear() {
        txt_fname.setText(null);
        txt_lname.setText(null);
        txt_nic.setText(null);
        txt_company.setText(null);
        txt_email.setText(null);
        txt_mobile.setText(null);
        txt_street.setText(null);
        txt_city.setText(null);
        txt_fax.setText(null);
    }

    public void updateData(String id) {
        try {
            ResultSet rs = DB.search("SELECT * FROM supplier WHERE id_supplier = '" + id + "'");
            if (rs.next()) {
                id_supplier.setText(rs.getString(1));
                txt_fname.setText(rs.getString(2));
                txt_lname.setText(rs.getString(3));
                txt_nic.setText(rs.getString(4));
                txt_company.setText(rs.getString(5));
                txt_email.setText(rs.getString(6));
                txt_mobile.setText(rs.getString(7));
                txt_fax.setText(rs.getString(8));
                txt_street.setText(rs.getString(9));
                txt_city.setText(rs.getString(10));
                if (rs.getInt(11) == 1) {
                    active.setSelected(true);
                } else {
                    inactive.setSelected(true);
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
